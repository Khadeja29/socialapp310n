                              ))),
                    ),
                    Positioned(
                        bottom: 0,
                        right: 0,
                        child: Container(
                          height: 40,
                          width: 40,
                          decoration: BoxDecoration(
                            shape: BoxShape.circle,
                            border: Border.all(
                              width: 4,
                              color: Theme.of(context).scaffoldBackgroundColor,
                            ),
                            color: AppColors.darkpurple,
                          ),
                          child: Icon(
                            Icons.edit,
                            color: Colors.white,
                          ),
                        )),
                  ],
                ),
              ),
              SizedBox(
                height: 35,
              ),
              buildTextField("Username", "@doggo", false),
              buildTextField("Full name", "Generic Name", false),
              buildTextField("Bio", "This is a generic sentence. This is a generic sentence. This is a generic sentence doggo bio.", false),
              Row(
                children: [
                  Checkbox(
                    value: private,
                    activeColor: AppColors.darkgrey,
                    onChanged: (value) {
                      setState(() {
                        private = value;
                      });
                    },
                  ),
                  Text("Make account private"),
                  Spacer()
                ],
              ),
              SizedBox(
                height: 35,
              ),
              Row(
                mainAxisAlignment: MainAxisAlignment.spaceBetween,
                children: [
                  OutlineButton(
                    padding: EdgeInsets.symmetric(horizontal: 50),
                    shape: RoundedRectangleBorder(
                        borderRadius: BorderRadius.circular(20)),
                    onPressed: () {},
